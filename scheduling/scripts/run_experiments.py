#!/usr/bin/env python3
"""
Run Allegra the Barman scheduling experiments and build patron-level files.

New project layout expected:

    <project_root>/
    ├── Makefile
    ├── results/
    └── scripts/
        └── run_experiments.py

Experiment design:
- sched in [0, 3] inclusive
- seed in [1, 3] inclusive
- s/context-switch time = 0
- noPatrons candidates in [8, 50] inclusive
- for each seed, randomly choose 3 noPatrons values using that seed
- run all combinations of sched x seed x chosen noPatrons

Java command:
    make run ARGS="<noPatrons> <sched> <s> <seed>"

Expected raw Java outputs:
    results/OrderData/<ALG>_<noPatrons>_<seed>.txt
    results/OrderMetrics/<ALG>_<noPatrons>_<seed>.txt

Generated by this script after each successful run:
    results/PatronData/<ALG>_<noPatrons>_<seed>.txt
    results/PatronMetrics/<ALG>_<noPatrons>_<seed>.txt

Cleaning behaviour:
    If planned output files already exist, this script deletes the entire
    results/ directory and starts recording afresh.

    This is now safe because scripts/ is outside results/.
"""

from __future__ import annotations

import argparse
import csv
import random
import shutil
import subprocess
import sys
import time
from dataclasses import dataclass
from pathlib import Path


SCHEDULER_NAMES = {
    0: "FCFS",
    1: "SJF",
    2: "PRIORITY",
    3: "MLFQ",
}


GENERATED_RESULT_DIRS = [
    "OrderData",
    "OrderMetrics",
    "PatronData",
    "PatronMetrics",
    "StatMetrics",
]


@dataclass(frozen=True)
class Experiment:
    scheduler_code: int
    scheduler_name: str
    seed: int
    no_patrons: int
    context_switch_time: int

    @property
    def args_string(self) -> str:
        return (
            f"{self.no_patrons} "
            f"{self.scheduler_code} "
            f"{self.context_switch_time} "
            f"{self.seed}"
        )

    @property
    def file_name(self) -> str:
        return f"{self.scheduler_name}_{self.no_patrons}_{self.seed}.txt"

    @property
    def log_stem(self) -> str:
        return (
            f"{self.scheduler_name}"
            f"_N{self.no_patrons}"
            f"_S{self.context_switch_time}"
            f"_Seed{self.seed}"
        )


def parse_args() -> argparse.Namespace:
    script_path = Path(__file__).resolve()

    # Expected:
    #   <project_root>/scripts/run_experiments.py
    #
    # parents[0] = scripts
    # parents[1] = project root
    default_project_root = script_path.parents[1]

    parser = argparse.ArgumentParser(
        description="Run scheduling experiments and generate patron-level outputs."
    )

    parser.add_argument(
        "--project-root",
        type=Path,
        default=default_project_root,
        help="Project root containing the Makefile. Default: inferred from script location.",
    )

    parser.add_argument("--min-patrons", type=int, default=8)
    parser.add_argument("--max-patrons", type=int, default=50)
    parser.add_argument("--samples-per-seed", type=int, default=3)
    parser.add_argument("--seed-start", type=int, default=1)
    parser.add_argument("--seed-end", type=int, default=3)
    parser.add_argument("--sched-start", type=int, default=0)
    parser.add_argument("--sched-end", type=int, default=3)
    parser.add_argument("--context-switch-time", type=int, default=0)

    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Show the experiment plan without running make or deleting results.",
    )

    parser.add_argument(
        "--stop-on-failure",
        action="store_true",
        help="Stop immediately if one simulation run fails.",
    )

    parser.add_argument(
        "--force-clean",
        action="store_true",
        help="Delete results/ before running even if no planned output files exist.",
    )

    return parser.parse_args()


def choose_no_patrons(
    seed: int,
    min_patrons: int,
    max_patrons: int,
    sample_count: int,
) -> list[int]:
    if min_patrons > max_patrons:
        raise ValueError("min_patrons cannot be greater than max_patrons.")

    population = list(range(min_patrons, max_patrons + 1))

    if sample_count > len(population):
        raise ValueError(
            f"samples_per_seed={sample_count} is larger than the available patron range."
        )

    rng = random.Random(seed)
    return sorted(rng.sample(population, sample_count))


def build_experiments(args: argparse.Namespace) -> list[Experiment]:
    experiments: list[Experiment] = []

    for seed in range(args.seed_start, args.seed_end + 1):
        selected_no_patrons = choose_no_patrons(
            seed=seed,
            min_patrons=args.min_patrons,
            max_patrons=args.max_patrons,
            sample_count=args.samples_per_seed,
        )

        for scheduler_code in range(args.sched_start, args.sched_end + 1):
            scheduler_name = SCHEDULER_NAMES.get(
                scheduler_code,
                f"SCHED{scheduler_code}",
            )

            for no_patrons in selected_no_patrons:
                experiments.append(
                    Experiment(
                        scheduler_code=scheduler_code,
                        scheduler_name=scheduler_name,
                        seed=seed,
                        no_patrons=no_patrons,
                        context_switch_time=args.context_switch_time,
                    )
                )

    return experiments


def output_files_for_experiment(results_dir: Path, experiment: Experiment) -> list[Path]:
    file_name = experiment.file_name

    return [
        results_dir / "OrderData" / file_name,
        results_dir / "OrderMetrics" / file_name,
        results_dir / "PatronData" / file_name,
        results_dir / "PatronMetrics" / file_name,
    ]


def planned_outputs_exist(results_dir: Path, experiments: list[Experiment]) -> bool:
    if not results_dir.exists():
        return False

    for experiment in experiments:
        for output_file in output_files_for_experiment(results_dir, experiment):
            if output_file.exists():
                return True

    return False


def reset_results_dir(results_dir: Path) -> None:
    """
    Deletes results/ completely and recreates the expected result subdirectories.

    This is safe because this script now lives in scripts/, not results/scripts/.
    """
    if results_dir.exists():
        shutil.rmtree(results_dir)

    results_dir.mkdir(parents=True, exist_ok=True)

    for dirname in GENERATED_RESULT_DIRS:
        (results_dir / dirname).mkdir(parents=True, exist_ok=True)


def ensure_results_dirs(results_dir: Path) -> None:
    results_dir.mkdir(parents=True, exist_ok=True)

    for dirname in GENERATED_RESULT_DIRS:
        (results_dir / dirname).mkdir(parents=True, exist_ok=True)


def write_selected_patrons(script_dir: Path, args: argparse.Namespace) -> Path:
    output_path = script_dir / "selected_noPatrons.tsv"

    with output_path.open("w", newline="", encoding="utf-8") as f:
        writer = csv.writer(f, delimiter="\t")
        writer.writerow(["seed", "selected_noPatrons"])

        for seed in range(args.seed_start, args.seed_end + 1):
            selected = choose_no_patrons(
                seed=seed,
                min_patrons=args.min_patrons,
                max_patrons=args.max_patrons,
                sample_count=args.samples_per_seed,
            )
            writer.writerow([seed, ",".join(str(x) for x in selected)])

    return output_path


def run_experiment(
    experiment: Experiment,
    project_root: Path,
    run_logs_dir: Path,
    dry_run: bool,
) -> tuple[int, float, Path, Path]:
    stdout_path = run_logs_dir / f"{experiment.log_stem}.stdout.txt"
    stderr_path = run_logs_dir / f"{experiment.log_stem}.stderr.txt"

    command = ["make", "run", f"ARGS={experiment.args_string}"]

    start = time.perf_counter()

    if dry_run:
        stdout_path.write_text(
            "DRY RUN ONLY\n"
            f"Command: {' '.join(command)}\n",
            encoding="utf-8",
        )
        stderr_path.write_text("", encoding="utf-8")
        return 0, time.perf_counter() - start, stdout_path, stderr_path

    with stdout_path.open("w", encoding="utf-8") as stdout_file, \
            stderr_path.open("w", encoding="utf-8") as stderr_file:
        completed = subprocess.run(
            command,
            cwd=project_root,
            stdout=stdout_file,
            stderr=stderr_file,
            text=True,
            check=False,
        )

    return completed.returncode, time.perf_counter() - start, stdout_path, stderr_path


def split_primary_key(primary_key: str) -> tuple[int, int]:
    """
    primary_key format:
        patronID_seqNum

    Example:
        5_3 -> patron_id=5, seq_num=3
    """
    patron_text, seq_text = primary_key.split("_", 1)
    return int(patron_text), int(seq_text)


def read_tsv(path: Path) -> list[dict[str, str]]:
    with path.open("r", newline="", encoding="utf-8") as f:
        reader = csv.DictReader(f, delimiter="\t")
        return list(reader)


def write_tsv(path: Path, fieldnames: list[str], rows: list[dict[str, object]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)

    with path.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames, delimiter="\t")
        writer.writeheader()

        for row in rows:
            writer.writerow(row)


def build_patron_files(results_dir: Path, experiment: Experiment) -> tuple[Path, Path]:
    """
    Builds:
        results/PatronData/<ALG>_<noPatrons>_<seed>.txt
        results/patronmetrics_dir/<ALG>_<noPatrons>_<seed>.txt

    from:
        results/OrderData/<ALG>_<noPatrons>_<seed>.txt
        results/ordermetrics_dir/<ALG>_<noPatrons>_<seed>.txt
    """
    order_file = results_dir / "OrderData" / experiment.file_name
    metrics_file = results_dir / "OrderMetrics" / experiment.file_name

    if not order_file.exists():
        raise FileNotFoundError(f"Missing order data file: {order_file}")

    if not metrics_file.exists():
        raise FileNotFoundError(f"Missing order metrics file: {metrics_file}")

    order_rows = read_tsv(order_file)
    metric_rows = read_tsv(metrics_file)

    metrics_by_key = {
        row["primary_key"]: row
        for row in metric_rows
    }

    patrons: dict[int, dict[str, object]] = {}

    for order_row in order_rows:
        primary_key = order_row["primary_key"]
        patron_id, _seq_num = split_primary_key(primary_key)

        metric_row = metrics_by_key.get(primary_key)

        if metric_row is None:
            raise ValueError(
                f"Missing metrics row for primary_key={primary_key} in {metrics_file}"
            )

        patron_arrival_time = int(order_row["patron_arrival_time"])
        order_arrival_time = int(order_row["order_arrival_time"])
        prep_time = int(order_row["prepTime"])
        order_completion_time = int(order_row["order_completion_time"])

        waiting_time = int(metric_row["waiting_time"])
        response_time = int(metric_row["response_time"])
        turnaround_time = int(metric_row["turnaround_time"])

        if patron_id not in patrons:
            patrons[patron_id] = {
                "patron_id": patron_id,
                "patron_arrival_time": patron_arrival_time,
                "num_of_drinks": 0,
                "first_order_arrival_time": order_arrival_time,
                "last_order_completion_time": order_completion_time,
                "total_prepTime": 0,
                "total_waiting_time": 0,
                "total_response_time": 0,
                "total_turnaround_time": 0,
                "max_waiting_time": 0,
                "max_response_time": 0,
                "max_turnaround_time": 0,
            }

        patron = patrons[patron_id]

        patron["num_of_drinks"] = int(patron["num_of_drinks"]) + 1
        patron["first_order_arrival_time"] = min(
            int(patron["first_order_arrival_time"]),
            order_arrival_time,
        )
        patron["last_order_completion_time"] = max(
            int(patron["last_order_completion_time"]),
            order_completion_time,
        )
        patron["total_prepTime"] = int(patron["total_prepTime"]) + prep_time
        patron["total_waiting_time"] = int(patron["total_waiting_time"]) + waiting_time
        patron["total_response_time"] = int(patron["total_response_time"]) + response_time
        patron["total_turnaround_time"] = (
            int(patron["total_turnaround_time"]) + turnaround_time
        )
        patron["max_waiting_time"] = max(
            int(patron["max_waiting_time"]),
            waiting_time,
        )
        patron["max_response_time"] = max(
            int(patron["max_response_time"]),
            response_time,
        )
        patron["max_turnaround_time"] = max(
            int(patron["max_turnaround_time"]),
            turnaround_time,
        )

    patron_data_rows: list[dict[str, object]] = []
    patron_metric_rows: list[dict[str, object]] = []

    for patron_id in sorted(patrons):
        patron = patrons[patron_id]

        process_turnaround_time = (
            int(patron["last_order_completion_time"])
            - int(patron["patron_arrival_time"])
        )

        patron_data_rows.append(
            {
                "patron_id": patron["patron_id"],
                "patron_arrival_time": patron["patron_arrival_time"],
                "num_of_drinks": patron["num_of_drinks"],
                "first_order_arrival_time": patron["first_order_arrival_time"],
                "last_order_completion_time": patron["last_order_completion_time"],
                "total_prepTime": patron["total_prepTime"],
            }
        )

        patron_metric_rows.append(
            {
                "patron_id": patron["patron_id"],
                "total_waiting_time": patron["total_waiting_time"],
                "total_response_time": patron["total_response_time"],
                "total_turnaround_time": patron["total_turnaround_time"],
                "process_turnaround_time": process_turnaround_time,
                "max_waiting_time": patron["max_waiting_time"],
                "max_response_time": patron["max_response_time"],
                "max_turnaround_time": patron["max_turnaround_time"],
            }
        )

    patron_data_file = results_dir / "PatronData" / experiment.file_name
    patron_metrics_file = results_dir / "patronmetrics_dir" / experiment.file_name

    write_tsv(
        patron_data_file,
        [
            "patron_id",
            "patron_arrival_time",
            "num_of_drinks",
            "first_order_arrival_time",
            "last_order_completion_time",
            "total_prepTime",
        ],
        patron_data_rows,
    )

    write_tsv(
        patron_metrics_file,
        [
            "patron_id",
            "total_waiting_time",
            "total_response_time",
            "total_turnaround_time",
            "process_turnaround_time",
            "max_waiting_time",
            "max_response_time",
            "max_turnaround_time",
        ],
        patron_metric_rows,
    )

    return patron_data_file, patron_metrics_file


def main() -> int:
    args = parse_args()

    project_root = args.project_root.resolve()
    results_dir = project_root / "results"
    script_dir = Path(__file__).resolve().parent
    run_logs_dir = script_dir / "run_logs"

    experiments = build_experiments(args)

    if not project_root.exists():
        print(f"ERROR: project root does not exist: {project_root}", file=sys.stderr)
        return 1

    makefile = project_root / "Makefile"
    if not makefile.exists() and not args.dry_run:
        print(
            f"ERROR: Makefile not found at {makefile}. "
            f"Use --project-root to point to the correct project directory.",
            file=sys.stderr,
        )
        return 1

    run_logs_dir.mkdir(parents=True, exist_ok=True)

    if args.dry_run:
        print("Dry run selected: results/ will not be deleted and make will not be run.")
    else:
        if args.force_clean or planned_outputs_exist(results_dir, experiments):
            print("Existing planned result files detected. Deleting results/ and starting afresh...")
            reset_results_dir(results_dir)
        else:
            ensure_results_dirs(results_dir)

    selected_path = write_selected_patrons(script_dir, args)
    experiment_log_path = script_dir / "experiment_log.tsv"

    print(f"Project root: {project_root}")
    print(f"Results dir: {results_dir}")
    print(f"Scripts dir: {script_dir}")
    print(f"Selected patron counts written to: {selected_path}")
    print(f"Experiment log will be written to: {experiment_log_path}")
    print(f"Run logs directory: {run_logs_dir}")
    print(f"Dry run: {args.dry_run}")
    print(f"Total runs planned: {len(experiments)}")

    failures = 0

    with experiment_log_path.open("w", newline="", encoding="utf-8") as f:
        writer = csv.writer(f, delimiter="\t")
        writer.writerow(
            [
                "run_number",
                "scheduler_code",
                "scheduler_name",
                "seed",
                "noPatrons",
                "context_switch_time",
                "command",
                "return_code",
                "duration_seconds",
                "stdout_log",
                "stderr_log",
                "patron_data_file",
                "patron_metrics_file",
            ]
        )

        for i, experiment in enumerate(experiments, start=1):
            command_text = f'make run ARGS="{experiment.args_string}"'

            print(
                f"[{i:03d}/{len(experiments):03d}] "
                f"{experiment.scheduler_name}, "
                f"noPatrons={experiment.no_patrons}, "
                f"seed={experiment.seed}, "
                f"s={experiment.context_switch_time}"
            )

            return_code, duration, stdout_path, stderr_path = run_experiment(
                experiment=experiment,
                project_root=project_root,
                run_logs_dir=run_logs_dir,
                dry_run=args.dry_run,
            )

            patron_data_file = ""
            patron_metrics_file = ""

            if return_code != 0:
                failures += 1
                print(
                    f"  FAILED with return code {return_code}. See {stderr_path}",
                    file=sys.stderr,
                )
            elif not args.dry_run:
                try:
                    patron_data_path, patron_metrics_path = build_patron_files(
                        results_dir=results_dir,
                        experiment=experiment,
                    )
                    patron_data_file = str(patron_data_path)
                    patron_metrics_file = str(patron_metrics_path)
                    print(f"  Wrote {patron_data_path}")
                    print(f"  Wrote {patron_metrics_path}")
                except Exception as exc:
                    failures += 1
                    print(
                        f"  FAILED while building patron files: {exc}",
                        file=sys.stderr,
                    )

            writer.writerow(
                [
                    i,
                    experiment.scheduler_code,
                    experiment.scheduler_name,
                    experiment.seed,
                    experiment.no_patrons,
                    experiment.context_switch_time,
                    command_text,
                    return_code,
                    f"{duration:.4f}",
                    stdout_path,
                    stderr_path,
                    patron_data_file,
                    patron_metrics_file,
                ]
            )

            if failures and args.stop_on_failure:
                break

    if args.dry_run:
        print("Dry run complete. No simulations were executed.")
    elif failures:
        print(f"Completed with {failures} failed step(s). Check logs.")
    else:
        print("All simulations completed successfully.")

    return 0 if failures == 0 else 1


if __name__ == "__main__":
    raise SystemExit(main())
